// Boolean value is either true or false.
//     - Write three JavaScript statement which provide truthy value.
//     - Write three JavaScript statement which provide falsy value.

// truthy value
let var1 = 12 > 10;
console.log(var1);

let var2 = 12 > 15;
console.log(var1);

let var3 = "Hello" === "Hello";
console.log(var3);

// falsy value
let var4 = "10" === 10;
console.log(var4);

let var5 = 12 < 10;
console.log(var5);

let var6 = "Hello" === "hello";
console.log(var6);